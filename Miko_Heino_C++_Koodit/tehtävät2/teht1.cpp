#include <iostream>

int main() {
    std::cout << "int: " << sizeof(int) << " tavua" << '\n';
    std::cout << "unsigned int: " << sizeof(unsigned int) << " tavua" << '\n';
    std::cout << "float: " << sizeof(float) << " tavua" << '\n';
    std::cout << "double: " << sizeof(double) << " tavua" << '\n';
    std::cout << "char: " << sizeof(char) << " tavua" << '\n';
    std::cout << "bool: " << sizeof(bool) << " tavua" << '\n';

    return 0;
}
